    private void FillTeamCombo() {
        try{
            String sql = "SELECT * FROM Teams";
            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();
            
            while(rs.next()) {
                Team newTeam = new Team(rs.getInt("id"), rs.getString("name"));
                teamBox.addItem(newTeam);
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
