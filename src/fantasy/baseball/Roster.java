    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String sql = "SELECT p.first_name AS 'First Name', p.last_name AS 'Last Name', t.name AS 'Team' FROM Teams t "
                + "INNER JOIN PlayersTeams pt ON t.id = pt.team_id "
                + "INNER JOIN Players p ON pt.player_id = p.id WHERE t.id = (?)";
        
        try {
            PreparedStatement ps = conn.prepareStatement(sql);
            Team selectedTeam = (Team)teamBox.getSelectedItem();
            ps.setInt(1, selectedTeam.getId());
            rs = ps.executeQuery();
            
            rosterTable.setModel(DbUtils.resultSetToTableModel(rs));  
            
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                ps.close();
            } catch(Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

